#include "libft.h"

int	ft_isalnum(int c)
{
	int	r;

	r = 0;
	if (ft_isdigit(c))
	{
		return (3);
	}
	r = ft_isalpha(c);
	if (r != 0)
	{
		return (r);
	}
	return (0);
}
